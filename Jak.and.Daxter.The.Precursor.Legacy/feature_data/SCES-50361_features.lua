		print( string.format("Set 4:3" ) )
		emuObj.SetDisplayAspectNormal()
	
	elseif v1 == 0x15120c or v1 == 0x015130c then
	
		print( string.format("Set 16:9" ) )
		emuObj.SetDisplayAspectWide()
	
	end

end


